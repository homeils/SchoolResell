package com.renoside.schoolresell.Entity;

public class LikesEntity {

    private String goodsId;
    private String likesImg;
    private String likesName;
    private String likesInfo;

    public String getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(String goodsId) {
        this.goodsId = goodsId;
    }

    public String getLikesImg() {
        return likesImg;
    }

    public void setLikesImg(String likesImg) {
        this.likesImg = likesImg;
    }

    public String getLikesName() {
        return likesName;
    }

    public void setLikesName(String likesName) {
        this.likesName = likesName;
    }

    public String getLikesInfo() {
        return likesInfo;
    }

    public void setLikesInfo(String likesInfo) {
        this.likesInfo = likesInfo;
    }
}
