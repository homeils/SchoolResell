package com.renoside.schoolresell.Entity;

public class PutEntity {

    private String goodsId;
    private String putImg;
    private String putTitle;
    private String putDescription;
    private String putStatus;
    private String putOperation;

    public String getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(String goodsId) {
        this.goodsId = goodsId;
    }

    public String getPutImg() {
        return putImg;
    }

    public void setPutImg(String putImg) {
        this.putImg = putImg;
    }

    public String getPutTitle() {
        return putTitle;
    }

    public void setPutTitle(String putTitle) {
        this.putTitle = putTitle;
    }

    public String getPutDescription() {
        return putDescription;
    }

    public void setPutDescription(String putDescription) {
        this.putDescription = putDescription;
    }

    public String getPutStatus() {
        return putStatus;
    }

    public void setPutStatus(String putStatus) {
        this.putStatus = putStatus;
    }

    public String getPutOperation() {
        return putOperation;
    }

    public void setPutOperation(String putOperation) {
        this.putOperation = putOperation;
    }

}
